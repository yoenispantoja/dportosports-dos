  }
	
	/**
	 * Enqueue scripts
	 */
	public function enqueue() {

		wp_enqueue_style( 'newsmunch-icon-picker-css', NEWSMUNCH_THEME_URI . '/assets/fonticonpicker/jquery.fonticonpicker.min.css', false );
			 
		wp_enqueue_script( 'newsmunch-icon-picker-js', NEWSMUNCH_THEME_URI .'/assets/fonticonpicker/jquery.fonticonpicker.min.js', array( 'jquery', 'jquery-ui-sortable' ) );
	}
	
  /**
   * Render the content of the dropdown
   *
   * Adding the font-family styling to the select so that the font renders 
   * @return HTML
   */
  public function render_content() {
    if ( ! empty( $this->icons ) ) { ?>
      <label>
        <span class="customize-control-title" style="flex: 2 0 0; vertical-align: middle;"><?php echo esc_html( $this->label ); ?></span>
        <select <?php $this->link(); ?> style="font-family: 'Font Awesome 6 Free', Arial;" class="contact-icon-picker iconPicker">
          <?php
            foreach ( $this->icons as $k=>$v ) {
              printf('<option value="%s" %s>%s</option>', $k, selected($this->value(), $k, false), $v);
            }
          ?>
        </select>
      </label>
    <?php }
  }

